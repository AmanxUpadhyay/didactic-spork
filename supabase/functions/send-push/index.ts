/**
 * Decode a base64url string to Uint8Array.
 */
function base64urlToBytes(b64url: string): Uint8Array {
  const b64 = b64url.replace(/-/g, "+").replace(/_/g, "/");
  const pad = (4 - (b64.length % 4)) % 4;
  const padded = b64 + "=".repeat(pad);
  const binary = atob(padded);
  const bytes = new Uint8Array(binary.length);
  for (let i = 0; i < binary.length; i++) {
    bytes[i] = binary.charCodeAt(i);
  }
  return bytes;
}

/**
 * Encode Uint8Array to base64url string (no padding).
 */
function bytesToBase64url(bytes: Uint8Array): string {
  let binary = "";
  for (const b of bytes) {
    binary += String.fromCharCode(b);
  }
  return btoa(binary).replace(/\+/g, "-").replace(/\//g, "_").replace(/=+$/, "");
}

/**
 * Convert raw base64url-encoded VAPID keys (as generated by web-push CLI)
 * into the JWK-based ExportedVapidKeys format that @negrel/webpush expects.
 *
 * Public key: 65 bytes uncompressed EC point (0x04 || x[32] || y[32])
 * Private key: 32 bytes raw "d" parameter
 */
function rawKeysToJwk(
  publicKeyB64: string,
  privateKeyB64: string,
): { publicKey: JsonWebKey; privateKey: JsonWebKey } {
  const pubBytes = base64urlToBytes(publicKeyB64);
  const privBytes = base64urlToBytes(privateKeyB64);

  // Extract x, y from uncompressed public key (skip 0x04 prefix)
  const x = bytesToBase64url(pubBytes.slice(1, 33));
  const y = bytesToBase64url(pubBytes.slice(33, 65));
  const d = bytesToBase64url(privBytes);

  const publicKey: JsonWebKey = {
    kty: "EC",
    crv: "P-256",
    x,
    y,
    ext: true,
  };

  const privateKey: JsonWebKey = {
    kty: "EC",
    crv: "P-256",
    x,
    y,
    d,
    ext: true,
  };

  return { publicKey, privateKey };
}

