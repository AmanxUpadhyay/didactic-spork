Deno.serve(async (req: Request) => {
  if (req.method === "OPTIONS") {
    return new Response("ok", { headers: corsHeaders });
  }

  if (req.method !== "POST") {
    return new Response(JSON.stringify({ error: "Method not allowed" }), {
      status: 405,
      headers: corsHeaders,
    });
  }

  const auth = verifyServiceRole(req.headers.get("Authorization"));
  if (auth.error) return auth.error;
  const supabase = auth.client;

  const vapidPublicKey = Deno.env.get("VAPID_PUBLIC_KEY");
  const vapidPrivateKey = Deno.env.get("VAPID_PRIVATE_KEY");

  if (!vapidPublicKey || !vapidPrivateKey) {
    return new Response(
      JSON.stringify({ error: "VAPID keys not configured" }),
      { status: 500, headers: corsHeaders },
    );
  }

  // Convert raw base64url keys to JWK and import as CryptoKeyPair
  const jwk = rawKeysToJwk(vapidPublicKey, vapidPrivateKey);
  const vapidKeys = await importVapidKeys(jwk);

  // Create ApplicationServer instance
  const appServer = await ApplicationServer.new({
    contactInformation: "mailto:hello@jugalbandi.app",
    vapidKeys,
  });

  try {
    const { data: queue, error: queueError } = await supabase
      .from("notification_queue")
      .select(
        "id, user_id, category, title, body, data, actions, tag, urgency, status, scheduled_for, retry_count, max_retries",
      )
      .eq("status", "pending")
      .lte("scheduled_for", new Date().toISOString())
      .order("scheduled_for", { ascending: true })
      .limit(BATCH_SIZE);

    if (queueError) {
      console.error("Failed to query notification_queue:", queueError);
      return new Response(
        JSON.stringify({ error: "Failed to query queue", detail: queueError.message }),
        { status: 500, headers: corsHeaders },
      );
    }

    if (!queue || queue.length === 0) {
      return new Response(
        JSON.stringify({ processed: 0, delivered: 0, failed: 0, rescheduled: 0 }),
        { status: 200, headers: corsHeaders },
      );
    }

    // Sort high-urgency first
    const urgencyOrder: Record<string, number> = {
      high: 0,
      normal: 1,
      low: 2,
      "very-low": 3,
    };
    queue.sort((a, b) => {
      const ua = urgencyOrder[a.urgency || "normal"] ?? 1;
      const ub = urgencyOrder[b.urgency || "normal"] ?? 1;
      if (ua !== ub) return ua - ub;
      return new Date(a.scheduled_for).getTime() - new Date(b.scheduled_for).getTime();
    });

    const summary: ProcessingSummary = {
      processed: queue.length,
      delivered: 0,
      failed: 0,
      rescheduled: 0,
    };

    for (const notification of queue as QueuedNotification[]) {
      await processNotification(supabase, notification, appServer, summary);
    }

    return new Response(JSON.stringify(summary), {
      status: 200,
      headers: corsHeaders,
    });
  } catch (err) {
    console.error("send-push unexpected error:", err);
    return new Response(
      JSON.stringify({ error: "Internal error", detail: String(err) }),
      { status: 500, headers: corsHeaders },
    );
  }
});
