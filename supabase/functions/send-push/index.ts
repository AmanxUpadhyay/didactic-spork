async function processNotification(
  supabase: SupabaseClient,
  notification: QueuedNotification,
  appServer: ApplicationServer,
  summary: ProcessingSummary,
): Promise<void> {
  await supabase
    .from("notification_queue")
    .update({ status: "processing" })
    .eq("id", notification.id);

  // 5:1 ratio guard: suppress negative-valence notifications if ratio is unhealthy
  const negativeCategories = ["streak_warning", "nudge", "task_deadline"];
  if (negativeCategories.includes(notification.category)) {
    const suppress = await shouldSuppressNegative(supabase, notification.user_id);
    if (suppress) {
      await supabase
        .from("notification_queue")
        .update({
          status: "cancelled",
          error_message: "Suppressed: 5:1 ratio unhealthy",
        })
        .eq("id", notification.id);

      await logAttempt(
        supabase, notification.id, null, "cancelled",
        "Suppressed by 5:1 ratio enforcement", 0,
      );
      summary.failed++;
      return;
    }
  }

  const { data: subscriptions } = await supabase
    .from("push_subscriptions")
    .select("id, endpoint, p256dh, auth_key")
    .eq("user_id", notification.user_id)
    .eq("active", true);

  if (!subscriptions || subscriptions.length === 0) {
    await supabase
      .from("notification_queue")
      .update({
        status: "failed",
        error_message: "No active push subscriptions",
        error_code: 0,
      })
      .eq("id", notification.id);

    await logAttempt(supabase, notification.id, null, "failed", "No active push subscriptions", 0);
    summary.failed++;
    return;
  }

  const { data: prefs } = await supabase
    .from("notification_preferences")
    .select("enabled, quiet_hours_start, quiet_hours_end, categories_enabled, timezone")
    .eq("user_id", notification.user_id)
    .maybeSingle();

  const preferences: NotificationPreferences = prefs || {
    enabled: true,
    quiet_hours_start: null,
    quiet_hours_end: null,
    categories_enabled: null,
    timezone: "Europe/London",
  };

  if (!preferences.enabled) {
    await supabase
      .from("notification_queue")
      .update({ status: "cancelled", error_message: "Notifications disabled by user" })
      .eq("id", notification.id);

    await logAttempt(supabase, notification.id, null, "cancelled", "Notifications disabled by user", 0);
    summary.failed++;
    return;
  }

  if (
    preferences.categories_enabled &&
    preferences.categories_enabled[notification.category] === false
  ) {
    await supabase
      .from("notification_queue")
      .update({ status: "cancelled", error_message: `Category '${notification.category}' disabled` })
      .eq("id", notification.id);

    await logAttempt(
      supabase, notification.id, null, "cancelled",
      `Category '${notification.category}' disabled`, 0,
    );
    summary.failed++;
    return;
  }

  const now = new Date();
  if (notification.urgency !== "high" && isInQuietHours(preferences, now)) {
    const rescheduleTime = getQuietHoursEndUTC(preferences);
    await supabase
      .from("notification_queue")
      .update({
        status: "pending",
        scheduled_for: rescheduleTime,
        error_message: "Rescheduled: quiet hours",
      })
      .eq("id", notification.id);

    await logAttempt(
      supabase, notification.id, null, "rescheduled",
      `Quiet hours — rescheduled to ${rescheduleTime}`, 0,
    );
    summary.rescheduled++;
    return;
  }

  const pushData = buildPayloadJson(notification);
  let anySuccess = false;

  for (const sub of subscriptions as Subscription[]) {
    const pushSubscription: WebPushSubscription = {
      endpoint: sub.endpoint,
      keys: {
        p256dh: sub.p256dh,
        auth: sub.auth_key,
      },
    };

    try {
      const subscriber = appServer.subscribe(pushSubscription);
      await subscriber.pushTextMessage(pushData, {
        urgency: mapUrgency(notification.urgency),
        topic: notification.tag || notification.category,
        ttl: 3600,
      });

      // Success
      anySuccess = true;

      await supabase
        .from("push_subscriptions")
        .update({ last_successful_push: new Date().toISOString() })
        .eq("id", sub.id);

      await logAttempt(supabase, notification.id, sub.id, "delivered", null, 201);
    } catch (err) {
      if (err instanceof PushMessageError) {
        const statusCode = err.response?.status ?? 0;

        if (err.isGone() || statusCode === 404) {
          // Dead subscription
          await supabase
            .from("push_subscriptions")
            .update({
              active: false,
              deactivated_at: new Date().toISOString(),
            })
            .eq("id", sub.id);

          await logAttempt(
            supabase, notification.id, sub.id, "subscription_expired",
            `Push endpoint returned ${statusCode}`, statusCode,
          );
        } else if (statusCode === 429 || statusCode >= 500) {
          await logAttempt(
            supabase, notification.id, sub.id, "error",
            `Push endpoint returned ${statusCode}`, statusCode,
          );
        } else {
          const errorBody = await err.response?.text().catch(() => "") ?? "";
          await logAttempt(
            supabase, notification.id, sub.id, "error",
            `HTTP ${statusCode}: ${errorBody.slice(0, 200)}`, statusCode,
          );
        }
      } else {
        await logAttempt(
          supabase, notification.id, sub.id, "error",
          `Exception: ${String(err).slice(0, 200)}`, 0,
        );
      }
    }
  }

  if (anySuccess) {
    await supabase
      .from("notification_queue")
      .update({
        status: "delivered",
        sent_at: new Date().toISOString(),
      })
      .eq("id", notification.id);

    summary.delivered++;
  } else {
    const newRetryCount = notification.retry_count + 1;

    if (newRetryCount < notification.max_retries) {
      await supabase
        .from("notification_queue")
        .update({
          status: "pending",
          retry_count: newRetryCount,
          error_message: "All subscriptions failed, will retry",
        })
        .eq("id", notification.id);
    } else {
      await supabase
        .from("notification_queue")
        .update({
          status: "failed",
          retry_count: newRetryCount,
          error_message: "Max retries exhausted",
        })
        .eq("id", notification.id);
    }

    summary.failed++;
  }
}

async function logAttempt(
  supabase: SupabaseClient,
  queueId: string,
  subscriptionId: string | null,
  status: string,
  errorMessage: string | null,
  errorCode: number,
): Promise<void> {
  await supabase.from("notification_log").insert({
    queue_id: queueId,
    subscription_id: subscriptionId,
    status,
    error_message: errorMessage,
    error_code: errorCode || null,
  });
}

