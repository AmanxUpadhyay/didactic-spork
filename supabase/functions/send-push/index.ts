interface QueuedNotification {
  id: string;
  user_id: string;
  category: string;
  title: string;
  body: string;
  data: Record<string, unknown> | null;
  actions: { action: string; title: string }[] | null;
  tag: string | null;
  urgency: string | null;
  status: string;
  scheduled_for: string;
  retry_count: number;
  max_retries: number;
}

interface Subscription {
  id: string;
  endpoint: string;
  p256dh: string;
  auth_key: string;
}

interface NotificationPreferences {
  enabled: boolean;
  quiet_hours_start: string | null;
  quiet_hours_end: string | null;
  categories_enabled: Record<string, boolean> | null;
  timezone: string;
}

interface ProcessingSummary {
  processed: number;
  delivered: number;
  failed: number;
  rescheduled: number;
}

