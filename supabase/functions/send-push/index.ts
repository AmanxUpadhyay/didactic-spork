function parseHHMM(hhmm: string): number {
  const [h, m] = hhmm.split(":").map(Number);
  return h * 60 + m;
}

function isInQuietHours(
  prefs: NotificationPreferences,
  now: Date,
): boolean {
  if (!prefs.quiet_hours_start || !prefs.quiet_hours_end) return false;

  const fmt = new Intl.DateTimeFormat("en-GB", {
    timeZone: prefs.timezone || "Europe/London",
    hour: "2-digit",
    minute: "2-digit",
    hour12: false,
  });

  const localTime = fmt.format(now);
  const nowMinutes = parseHHMM(localTime);
  const startMinutes = parseHHMM(prefs.quiet_hours_start);
  const endMinutes = parseHHMM(prefs.quiet_hours_end);

  if (startMinutes <= endMinutes) {
    return nowMinutes >= startMinutes && nowMinutes < endMinutes;
  } else {
    return nowMinutes >= startMinutes || nowMinutes < endMinutes;
  }
}

function getQuietHoursEndUTC(prefs: NotificationPreferences): string {
  const now = new Date();
  const tz = prefs.timezone || "Europe/London";

  const dateFmt = new Intl.DateTimeFormat("en-CA", {
    timeZone: tz,
    year: "numeric",
    month: "2-digit",
    day: "2-digit",
  });
  const localDateStr = dateFmt.format(now);

  const endTime = prefs.quiet_hours_end || "07:00";
  const [endH, endM] = endTime.split(":").map(Number);

  const utcStr = now.toLocaleString("en-US", { timeZone: "UTC" });
  const tzStr = now.toLocaleString("en-US", { timeZone: tz });
  const offsetMs = new Date(utcStr).getTime() - new Date(tzStr).getTime();

  const localMidnight = new Date(localDateStr + "T00:00:00Z");
  const targetLocal = new Date(
    localMidnight.getTime() + endH * 3600_000 + endM * 60_000,
  );
  const targetUTC = new Date(targetLocal.getTime() + offsetMs);

  if (targetUTC.getTime() <= now.getTime()) {
    targetUTC.setTime(targetUTC.getTime() + 86_400_000);
  }

  return targetUTC.toISOString();
}

function buildPayloadJson(notification: QueuedNotification): string {
  return JSON.stringify({
    title: notification.title,
    body: notification.body,
    icon: "/icons/icon-192.png",
    badge: "/icons/icon-192.png",
    tag: notification.tag || notification.category,
    data: {
      url: notification.data?.url || "/",
      notificationId: notification.id,
      category: notification.category,
      ...notification.data,
    },
    actions: notification.actions || [],
  });
}

function mapUrgency(
  urgency: string | null,
): "very-low" | "low" | "normal" | "high" {
  switch (urgency) {
    case "high":
      return "high";
    case "low":
      return "low";
    case "very-low":
      return "very-low";
    default:
      return "normal";
  }
}

